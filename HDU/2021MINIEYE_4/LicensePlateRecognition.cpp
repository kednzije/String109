#include <iostream>
#include <cstring>
#include <cstdio>
using namespace std;
constexpr int MAXN = 105;

int cnt[MAXN];
char c;

int main() {
	int t;
	scanf("%d", &t);
	for(int case_ind = 1; case_ind <= t; case_ind++) {
		memset(cnt, 0, sizeof(cnt));
		for(int i = 1; i <= 30; i++) {
			for(int j = 1; j <= 100; j++) {
				c = getchar();
				if(c == '#') {
					cnt[j]++;
				}
			}
			getchar();
		}
		printf("Case #%d:\n", case_ind);
		for(int i = 1; i <= 100; i++) {
			if(cnt[i]) {
				int j = i;
				while(cnt[j]) {
					j++;
				}
				if(j - i == 3) {
					j = i + 14;
				}
				if(j - i == 9 && cnt[j - 1] == 6) {
					j = i + 15;
				}
				printf("%d %d\n", i - 1, j - 2);
				i = j - 1;
			}
		}
	}
}
